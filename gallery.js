const gallerySection = document.getElementById("gallerySection");


// CREATE YEARS

for (const year in galleryData) {

  const yearSection = document.createElement("div");
  yearSection.classList.add("year-section");

  yearSection.innerHTML = `
    <h2 class="year-title">${year}</h2>
  `;

  const monthContainer = document.createElement("div");
  monthContainer.classList.add("month-container");

  yearSection.appendChild(monthContainer);


  // CREATE MONTHS

  for (const month in galleryData[year]) {

    const monthBtn = document.createElement("button");
    monthBtn.classList.add("month-btn");
    monthBtn.textContent = month;

    monthContainer.appendChild(monthBtn);


    // IMAGE GRID

    const imageGrid = document.createElement("div");
    imageGrid.classList.add("image-grid");

    if (galleryData[year][month].length === 0) {

      imageGrid.innerHTML = `
      <p class="empty-message">
      No memories have been made yet
      </p>
      `;

    }

    galleryData[year][month].forEach(photo => {

      const imageCard = document.createElement("div");
      imageCard.classList.add("image-card");

      imageCard.innerHTML = `
        <img src="gallery/${year}/${month.toLowerCase()}/${photo.file}">
        
        <div class="image-info">
          <h4>${photo.title}</h4>
        </div>
      `;

      imageGrid.appendChild(imageCard);


      // MODAL OPEN

      imageCard.addEventListener("click", () => {

        document.getElementById("imageModal").style.display = "flex";

        document.getElementById("modalImage").src =
          `gallery/${year}/${month.toLowerCase()}/${photo.file}`;

        document.getElementById("modalTitle").textContent =
          photo.title;

      });

    });

    yearSection.appendChild(imageGrid);


    // TOGGLE MONTH

    monthBtn.addEventListener("click", () => {

      const isVisible = imageGrid.style.display === "grid";

      // close if already open
      if (isVisible) {
        imageGrid.style.display = "none";
        monthBtn.classList.remove("active");
        return;
      }

      // close all other grids in same year
      const allGrids = yearSection.querySelectorAll(".image-grid");
      const allButtons = yearSection.querySelectorAll(".month-btn");

      allGrids.forEach(grid => {
        grid.style.display = "none";
      });

      allButtons.forEach(btn => {
        btn.classList.remove("active");
      });

      // open clicked one
      imageGrid.style.display = "grid";
      monthBtn.classList.add("active");

    });

    }

  gallerySection.appendChild(yearSection);

}


// CLOSE MODAL

document.getElementById("closeModal").addEventListener("click", () => {

  document.getElementById("imageModal").style.display = "none";

});


// CLOSE MODAL WHEN CLICKING OUTSIDE

document.getElementById("imageModal").addEventListener("click", (e) => {

  if (e.target.id === "imageModal") {

    document.getElementById("imageModal").style.display = "none";

  }

});